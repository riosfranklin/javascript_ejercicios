// Comprobar si una variable tiene una serie de valores distintos con switch.

var name = "Max";

switch (name) {
    case "Max":
        console.log("Ese es mi nombre!");
        break;
    case "Tom":
        console.log("Ese no es mi nombre!");
        break;
    default:
        console.log("Eso no es ni siquiera un nombre!");
}